************ Orientação a objeto******************
é um conceito que está relacionado com a ideia de classificar, organizar e abstrair coisas

Classe = classe é um projeto do objeto, antes de criar umk objeto deve-se definir a classe na qual ele pertence

Ex.:
Cachorro -          Tipo = Classe
tamanho int -       Atributos = Váriaveis
Raça string -       Atributos = Váriaveis
Latir () metodo     Ações = Métodos

Objeto é a instancia de uma classe, cria-se um objeto após definir uma classe para o mesmo. Na classe o cachorro, 
pode-se ter vários objetos, cada objeto pode possuir um atributo diferente

Atributos são váriaveis da classe e os métodos são Ações de uma classe


