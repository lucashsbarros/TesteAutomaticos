    Super Classe = Animais
        Quadrúpedes = Subclasses
        Aquáticos = Subclasses
        Aves = Subclasses

*************** ENCAPSULAR ***********************
É seárar o programa em partes o mais isoladas possiveis, podendo ou não mostrar essa parte.

    private - não pode ser acessado fora da classe, nem mesmo por outras classes derivadas da classe base(sua classes menores)
    protected - O membro não pode ser acessado fora da classe, porém pode ser acessada por sua classe base
    internal - o membro só é visivel na unidade código onde o mesmo está definido. Pode ser acessado por classes definidas
    protected-internal - 
    public - torna o membro acessivel de fora da definição da classe.

